void WorldManager::init() {

    int row=0, col=0;

    for (std::size_t i=0; i<intMap.size(); i++) {

        row = static_cast<int>(i / rows);
        col = static_cast<int>(i % cols);

        tileMap.push_back(Tile());
        tileMap[i].init(intMap[i], row*128, col*128);

        //std::cout << row << ", " << col << ".";
        //if (col == 24) { std::cout << std::endl; }
    }
}

void WorldManager::update(WindowManager& window) {

    float delta = window.frameTime / (1000.f / 60.f);
}

void WorldManager::draw(WindowManager& window) {

    for (std::size_t i=0; i<tileMap.size(); i++) {
        window.draw(tileMap[i]);
    }
}
